/**
 * 3. Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su
 * correspondiente traducción al castellano. Las palabras deben estar distribuidas
 * en dos columnas y alineadas a la izquierda.
 *
 * @author Elías Robles
 */
public class T01Ejercicio03 {
    public static void main(String[] args) {
        System.out.printf("%-13s %-13s\n","nature","naturaleza");
        System.out.printf("%-13s %-13s\n","apple","manzana");
        System.out.printf("%-13s %-13s\n","hello","hola");
        System.out.printf("%-13s %-13s\n","actually","de hecho");
        System.out.printf("%-13s %-13s\n","pineapple","piña");
        System.out.printf("%-13s %-13s\n","motorbike","motocicleta");
        System.out.printf("%-13s %-13s\n","car","coche");
        System.out.printf("%-13s %-13s\n","oil","aceite");
        System.out.printf("%-13s %-13s\n","forget","olvidar");
        System.out.printf("%-13s %-13s\n","forgive","perdonar");
    }
}
